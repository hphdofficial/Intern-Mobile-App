package com.android.mobile.models;

public class ClubModel {
    private int id;
    private String ten;

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    @Override
    public String toString() {
        return id + " - " + ten;
    }
}
